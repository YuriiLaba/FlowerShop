public class FlowerSpec {
    protected double length = 0;
    protected boolean fresh = false;
    protected double price = 0;



    public FlowerSpec(double length, boolean fresh, double price){
        this.price = price;

        this.length = length;

        this.fresh = fresh;
    }


}
